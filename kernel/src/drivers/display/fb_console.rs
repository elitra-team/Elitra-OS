#[no_mangle]
pub unsafe extern "C" fn fb_console_get_bg_at(x: u32, y: u32) -> u32 {
    if x >= FB_WIDTH || y >= FB_HEIGHT || FB_PTR.is_null() { return 0; }
    if FB_BPP == 32 {
        let pitch_dwords = (FB_PITCH / 4) as usize;
        let idx = (y as usize) * pitch_dwords + (x as usize);
        ptr::read_volatile((FB_PTR as *const u32).add(idx))
    } else if FB_BPP == 24 {
        let off = (y as usize) * (FB_PITCH as usize) + (x as usize) * 3;
        let b = ptr::read_volatile(FB_PTR.add(off)) as u32;
        let g = ptr::read_volatile(FB_PTR.add(off + 1)) as u32;
        let r = ptr::read_volatile(FB_PTR.add(off + 2)) as u32;
        (r << 16) | (g << 8) | b
    } else {
        0
    }
}
