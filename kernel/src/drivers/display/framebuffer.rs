#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_init(
    phys_addr: u64, width: u32, height: u32, pitch: u32, bpp: u8,
) {
    FB.phys_addr = phys_addr;
    FB.width = width;
    FB.height = height;
    FB.pitch = pitch;
    FB.bpp = bpp;
    FB.present = true;
    FB32 = phys_addr as *mut u32;
    FB8 = phys_addr as *mut u8;
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_get_fb_ptr() -> *mut u8 {
    FB8
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_info() -> FBInfo {
    FB
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_put_pixel(x: u32, y: u32, color: u32) {
    if !FB.present || x >= FB.width || y >= FB.height {
        return;
    }
    if FB.bpp == 32 {
        let pitch_dwords = (FB.pitch / 4) as usize;
        let idx = (y as usize) * pitch_dwords + (x as usize);
        ptr::write_volatile(FB32.add(idx), color);
    } else if FB.bpp == 24 {
        let off = (y as usize) * (FB.pitch as usize) + (x as usize) * 3;
        ptr::write_volatile(FB8.add(off), ((color >> 16) & 0xFF) as u8);
        ptr::write_volatile(FB8.add(off + 1), ((color >> 8) & 0xFF) as u8);
        ptr::write_volatile(FB8.add(off + 2), (color & 0xFF) as u8);
    } else if FB.bpp == 16 {
        let pitch_words = (FB.pitch / 2) as usize;
        let r = (color >> 16) & 0xFF;
        let g = (color >> 8) & 0xFF;
        let b = color & 0xFF;
        let fb16 = FB8 as *mut u16;
        let val = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
        ptr::write_volatile(fb16.add((y as usize) * pitch_words + (x as usize)), val as u16);
    }
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_fill_rect(x: u32, y: u32, w: u32, h: u32, color: u32) {
    if !FB.present {
        return;
    }
    let max_x = if x + w > FB.width { FB.width } else { x + w };
    let max_y = if y + h > FB.height { FB.height } else { y + h };

    if FB.bpp == 32 {
        let pitch_dwords = (FB.pitch / 4) as usize;
        for row in y..max_y {
            let line = FB32.add((row as usize) * pitch_dwords);
            for col in x..max_x {
                ptr::write_volatile(line.add(col as usize), color);
            }
        }
    } else {
        for row in y..max_y {
            for col in x..max_x {
                krust_framebuffer_put_pixel(col, row, color);
            }
        }
    }
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_clear(color: u32) {
    krust_framebuffer_fill_rect(0, 0, FB.width, FB.height, color);
}

#[no_mangle]
pub unsafe extern "C" fn krust_framebuffer_draw_char(x: u32, y: u32, c: i8, fg: u32, bg: u32) {
    if !FB.present || (c as u8) >= 128 {
        return;
    }
    let glyph = &FONT8X16[c as u8 as usize];
    for row in 0..16 {
        let bits = glyph[row];
        for col in 0..8 {
            let color = if (bits & (1 << (7 - col))) != 0 { fg } else { bg };
            krust_framebuffer_put_pixel(x + col as u32, y + row as u32, color);
        }
    }
}
