void init(uint64_t phys_addr, uint32_t width, uint32_t height,
          uint32_t pitch, uint8_t bpp) {
    fb.phys_addr = phys_addr;
    fb.width = width;
    fb.height = height;
    fb.pitch = pitch;
    fb.bpp = bpp;
    fb.present = true;

    // Map framebuffer (identity-mapped: phys == virt for low memory,
    // but framebuffer can be above 1MB so we need to map it)
    // The framebuffer address from GRUB is physical. We need it mapped.
    // Since the kernel is identity-mapped and we requested a framebuffer
    // below 4GB, it should be accessible. But if not, we need to page it.
    // For now, assume identity mapping works for the framebuffer region.

    fb32 = reinterpret_cast<uint32_t *>(fb.phys_addr);
    fb8 = reinterpret_cast<uint8_t *>(fb.phys_addr);

    drivers::NS16550::printf("fb: %dx%d %dbpp pitch=%d addr=0x%x\n",
                             width, height, bpp, pitch, phys_addr);
}

const FBInfo &info() {
    return fb;
}

void put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (!fb.present || x >= fb.width || y >= fb.height) return;

    if (fb.bpp == 32) {
        fb32[y * (fb.pitch / 4) + x] = color;
    } else if (fb.bpp == 24) {
        uint32_t off = y * fb.pitch + x * 3;
        fb8[off]     = (color >> 16) & 0xFF; // R
        fb8[off + 1] = (color >> 8) & 0xFF;  // G
        fb8[off]     = color & 0xFF;          // B
    } else if (fb.bpp == 16) {
        uint16_t *fb16 = reinterpret_cast<uint16_t *>(fb8);
        uint32_t r = (color >> 16) & 0xFF;
        uint32_t g = (color >> 8) & 0xFF;
        uint32_t b = color & 0xFF;
        fb16[y * (fb.pitch / 2) + x] = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
    }
}

void fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    if (!fb.present) return;
    uint32_t max_x = x + w;
    uint32_t max_y = y + h;
    if (max_x > fb.width) max_x = fb.width;
    if (max_y > fb.height) max_y = fb.height;

    if (fb.bpp == 32) {
        for (uint32_t row = y; row < max_y; row++) {
            uint32_t *line = &fb32[row * (fb.pitch / 4)];
            for (uint32_t col = x; col < max_x; col++) {
                line[col] = color;
            }
        }
    } else {
        for (uint32_t row = y; row < max_y; row++)
            for (uint32_t col = x; col < max_x; col++)
                put_pixel(col, row, color);
    }
}

void clear(uint32_t color) {
    fill_rect(0, 0, fb.width, fb.height, color);
}

void draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    if (!fb.present || static_cast<uint8_t>(c) >= 128) return;

    for (int row = 0; row < 16; row++) {
        uint8_t bits = font8x16[static_cast<uint8_t>(c)][row];
        for (int col = 0; col < 8; col++) {
            put_pixel(x + col, y + row, (bits & (1 << (7 - col))) ? fg : bg);
        }
    }
}
